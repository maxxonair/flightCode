package FlightElement.Target;

public class Target {
	
	private double RM;
	private int TARGET;	
	private double mu;
	private double Lt;
	

	public Target () {
		
	}


	public double getRM() {
		return RM;
	}


	public void setRM(double rM) {
		RM = rM;
	}


	public int getTARGET() {
		return TARGET;
	}


	public void setTARGET(int tARGET) {
		TARGET = tARGET;
	}


	public double getMu() {
		return mu;
	}


	public void setMu(double mu) {
		this.mu = mu;
	}


	public double getLt() {
		return Lt;
	}


	public void setLt(double lt) {
		Lt = lt;
	}
	
	

}
